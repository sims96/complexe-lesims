// Translation handler code
document.addEventListener('DOMContentLoaded', function() {
    // Add language switcher to all category pages
    const nav = document.querySelector('.back-button');
    const languageSwitcher = `
        <div class="language-switcher">
            <button class="lang-btn" data-lang="en">EN</button>
            <button class="lang-btn" data-lang="fr">FR</button>
        </div>
    `;
    nav.insertAdjacentHTML('afterend', languageSwitcher);

    // Initialize language preference
    let currentLang = localStorage.getItem('preferred-language') || 'en';

    // Function to update content based on selected language
    function updateContent(lang) {
        currentLang = lang;
        localStorage.setItem('preferred-language', lang);
        document.documentElement.lang = lang;

        // Special handling for drink prices (if they exist)
document.querySelectorAll('.drink-price').forEach(element => {
    // Keep the original price format for drinks
    const price = element.textContent.match(/[\d,]+/)[0];
    element.textContent = `${price} F`;
});

        // Update regular content
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            if (categoryTranslations[lang][key]) {
                if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                    element.placeholder = categoryTranslations[lang][key];
                } else {
                    element.textContent = categoryTranslations[lang][key];
                }
            }
        });

        // Update alt text translations
        document.querySelectorAll('[data-i18n-alt]').forEach(element => {
            const key = element.getAttribute('data-i18n-alt');
            if (categoryTranslations[lang][key]) {
                element.alt = categoryTranslations[lang][key];
            }
        });

        // Update prices if needed (keeping the original number)
        document.querySelectorAll('.price').forEach(element => {
            const price = element.textContent.match(/\d+/)[0];
            element.textContent = `${price} ${categoryTranslations[lang].currency}`;
        });

        // Update active state of buttons
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.remove('active');
            if (btn.getAttribute('data-lang') === lang) {
                btn.classList.add('active');
            }
        });
    }

    // Add event listeners to language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', (e) => {
            const lang = e.target.getAttribute('data-lang');
            updateContent(lang);
        });
    });

    // Initial content update
    updateContent(currentLang);
});